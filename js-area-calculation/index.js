let circleRadius = 5;
let circleArea = Math.PI * Math.pow(circleRadius, 2);
let circunference = 2 * Math.PI * circleRadius;
console.log(`For a circle with a radius of ${circleRadius.toFixed(2)} units:`);
console.log(`The area is approximately ${circleArea.toFixed(2)} square units.`);
console.log(`The circumference is approximately ${circunference.toFixed(2)} units.`);